    private void logActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logActionPerformed
        File Login = new File("Empleados.txt");
        try {
            FileReader fr= new FileReader(Login);
            BufferedReader read = new BufferedReader(fr);
            String line;
            while ((line=read.readLine())!=null) {
                String[] account = line.split(",");
                String userr = account[1];
                String passwordd = account[2];
                System.out.println(userr+","+passwordd);
                if (user.getText().equals(userr) && password.getText().equals(passwordd)) {
                    HomePage y = new HomePage();
                    y.setVisible(true);
                    dispose();
                }
            }

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Incorrect username or password");
        }
    }//GEN-LAST:event_logActionPerformed

